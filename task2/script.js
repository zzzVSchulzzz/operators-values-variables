//объявляю переменные
let degreesCelsius = prompt('Ввведите количество градусов по Цельсию')
    degreesFahrenheit = (degreesCelsius * 9/5) + 32 
//формула преобразования градусов Цельсия в Фаренгейта

//делаю вызов результата
alert(degreesCelsius + ' градусов по Цельсию равны ' + degreesFahrenheit + ' градусам по Фаренгейту.')